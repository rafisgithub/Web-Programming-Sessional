<?php

// Assignment Operators

$x = 10;
$x += 5; // 15
echo "x = $x\n";

$y = 20;
$y -= 10; // 10
echo "y = $y\n";

$z = 5;
$z *= 2; // 10
echo "z = $z\n";

$a = 15;
$a /= 3; // 5
echo "a = $a\n";

$b = 10;
$b %= 3; // 1
echo "b = $b\n";

$c = 5;
$c .= " is a string"; // "5 is a string"
echo "c = $c\n";

?>